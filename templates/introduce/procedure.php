 <p class="pd10 mr15 tline2 style6">프로그램의 주요 구성원에 대한 조사, 의견 수렴 및 필요한 평가는 매년 정기적으로 수행하여 교육방법(교육과정 포함)의 개선에 활용하고 있다. 프로그램 교육목표에 대한 종합적인 평가 및 개선은 3년 마다 시행한다. 졸업생들과 그 고용주의 의견, 재학생 의견은 설문조사로, 산업체 자문위원의 요구는 회의 및 수시 면담을 통하여 반영하고, 교수진의 의견은 프로그램위원회 회의를 통하여 의견을 반영한다.</p>

<p class="pd10 mr05 tline2 style6">본 프로그램에서는 교육목표의 지속적인 수정 보완을 위하여, 교과목과 학습 성과와의 정량적 상관관계를 이용한 학생 성취도 평가, 졸업생 및 고용주의 설문조사, 자문회의 등을 통하여 새로운 교육목표의 갱신, 그에 따른 학습 성과를 재수정한다. 그리고 이를 효과적으로 달성할 수 있도록 교습기술, 교육도구 등의 수정 보완에 노력하고 있다.
</p>
<p class="style6 tline2 mr20 pd10"><strong>(1) 프로그램 교육목표의 설정</strong></p>
<p class="pd10 mr05 tline2 style6">컴퓨터정보통신공학 프로그램에서는 교육목표를 설정하고 개선하는데 주요 구성원들의 의견을 체계적인 과정을 통하여 반영하고 있다. &lt;그림 2.2&gt;는 프로그램이 교육목표를 설정하고 평가하고 개선하는 시스템을 보여준다. 교육목표의 설정, 평가, 개선 등의 운영은 프로그램위원회를 중심으로 이루어진다.</p> 

<p class="pd10 mr05 tline2 style6">프로그램위원회는 프로그램의 주요 구성원들 즉, 학생, 교수진, 졸업생, 고용주, 자문위원의 의견을 바탕으로 졸업생들이 졸업후 2~3년에 갖추어야 할 능력을 파악하여 교육목표를 설정하였다. 이렇게 설정된 목표는, 대학의 요람, 학과 홈페이지 등 가능한 많은 곳에 공개하여 학생을 비롯한 주요 구성원이 인지하도록 한다. 교육목표에 대한 정기적인 평가 및 개선은 3년 주기로 이루어진다. 이를 위하여 주요 구성원의 의견뿐만 아니라, 그동안의 각종 평가들 즉, 교육목표 달성도, 학습성과 성취도, 기타 평가 결과를 고려하여 개선한다.

</p>

<p class="pd10 mr05 tline2 style6">본 프로그램에서는 공학인증 제도 시행 초기에 다음과 같이 교육목표의 수립과 개선에 관한 기본적인 원칙들을 정하였다.</p>
<p>&nbsp;</p>
<p class="pd10 mr05 tline2 style6">▶ 교육 목표 설정 및 개선에 관한 원칙</p>
<p class="pd10 mr05 tline2 style6">① 교육목표의 설정 및 개선에 프로그램 구성원의 의견을 최대한 수용한다. 특히, 학생들과 학과의 역량을 파악하고 교육목표가 이에 부합되어야 한다. (예: 입학생들의 수준, 졸업생들의 진로 등)</p>
<p class="pd10 mr05 tline2 style6">② 교육목표의 설정 및 개선뿐만 아니라 교육과정의 개선에, 연관 산업계 동향 및 고용주들의 의견을 가장 중요시 하도록 <br>한다.</p>
<p class="pd10 mr05 tline2 style6">③ 교육목표는 공학인증제도의 기본정신과 철학에 따라야 하고 홍익대학교 및 과학기술대학의 교육목표에 기초하여야 <br>한다.</p>
<p class="pd10 mr20 tline2 style6">컴퓨터정보통신공학 프로그램의 프로그램 교육목표의 설정은 다음 절차에 따라 수립되었다. &lt;그림 2.3&gt;은 교육목표의 수립시에 필요한 요구사항 수렴을 나타낸다.</p>
<p class="pd10 mr05 tline2 style6">① 공학교육인증원에서 요구하는 학습성과를 바탕으로 하고, 고용주 및 산업체 자문위원들의 면담을 통하여 졸업생들이 갖추어야 할 요구 사항을 파악하였다.</p>
<p class="pd10 mr05 tline2 style6">② 학과의 역량, 학생들의 수준과 특성, 졸업생들의 진출 현황 등을 고려하여 교육목표(안)를 수립하였다. 이때 교수진의 의견을 반영하였다.</p>
<p class="pd10 mr05 tline2 style6">③ 교육목표(안)을 다시 자문위원회를 통하여 의견을 듣고, 재학생과 졸업생 설문조사를 통해 적정성을 파악하고 <br>확정하였다.</p>
<p class="pd10 mr20 tline2 style6">본 프로그램에서는 공학교육인증 첫해인 2004년도에 공학인증제도의 원칙에 따라 본 학과의 역량을 파악하고 주요 구성원의 의견을 들어 1차 교육목표를 설정하고 공학인증 프로그램을 시작하였다. 2005년 과학기술대학의 공학인증지원센타 및 타과 자문교수의 개선 요청에 따라, 프로그램위원회에서 심도 있는 논의를 거쳐서 교육목표를 측정가능하게 개선하였다. 산업체 자문위원들의 의견을 들어 새로운 현재의 교육목표를 확정하였다. 그리고 교육목표에 대한 첫 번째의 평가는 3년 후인 2008년말에 진행된다.</p>
<p class="style6 tline2 mr20 pd10"><strong>(2) 학습 성과(Program Outcome; PO)의 결정</strong></p>
<p class="pd10 mr05 tline2 style6">올바른 공학교육을 위한 첫 단계는 본 프로그램의 목적에 맞는 적절한 교육목표를 설정하는 일이지만 교육목표의 달성도를 확인할 수 있는 학습 성과를 설정하고 객관적으로 평가하는 과정은 프로그램의 성공적인 운영에 있어 매우 중요하다. 공학교육인증원에서 요구하는 포괄적 기준을 이용하였으며 본 프로그램에 맞는 수행 준거를 사용하여 정량적인 측정이 이루어질 수 있도록 하였다. 교육목표와 학습 성과를 평가하는 데에는 학교 구성원뿐만 아니라 공학교육과 밀접한 관련이 있는 구성원들 간의 다양한 의견 취합이 이루어져야 한다. 이를 위해 본 프로그램에서는 외부자문위원회의, 각종 설문조사(재학생, 졸업생, 고용주) 등을 실시하여 관련 정보들을 수집하고 분석하여 의사 결정에 반영하도록 하였다. 프로그램의 학습성과는 3장에서 기술하였다. </p> 
<p class="style6 tline2 mr20 pd10"><strong>(3) 교육방법 개발 및 개선</strong></p>
<p class="pd10 mr05 tline2 style6">외부자문회의를 비롯한 프로그램의 주요 구성원의 의견을 들어 교육목표와 학습 성과 등을 결정한 후 교과과정위원회에서는 교과목을 ‘기본소양과목’, ‘공학기초과목’, ‘공학주제과목’으로 분류하여 개설 교과목을 결정하고 전체 교육기간에 적절하게 배분이 되도록 하였다. 교육과정위원회에서 결정된 교과과목은 프로그램위원회를 통하여 보다 심도 있는 토의 과정을 거쳐 수정 보완 된 후 최종적으로 결정되었다. 이보고서 4장에서 교육요소에 관하여 기술하였다.</p> 
<p class="style6 tline2 mr20 pd10"><strong>(4) 학생교육 및 평가</strong></p>
<p class="pd10 mr05 tline2 style6">교육과정과 교과과목이 결정되면 각 교과목의 담당 교수는 교과과목의 교육목표, 내용, 평가 방법 등을 포함하는 교과과목 포트폴리오를 작성하도록 하였다. 각 과목 교수들은 강의 별로 CQI를 포함한 교과과목 포트폴리오를 작성해서 학생들의 학습을 평가 한다. 교과 외적인 학습과 관련된 학업 성취 내용도 학생 포트폴리오에 포함시키도록 하였다. 이를 학생의 상담과 학습성과에 대한 평가에 이용하도록 하였다. </p> 
<p class="style6 tline2 mr20 pd10"><strong>(5) 교육 목표 평가 및 갱신</strong></p>
<p class="pd10 mr05 tline2 style6">체계적인 과정을 거쳐 결정된 교육목표는 영구 불변적일 수는 없다. 시대적 변화, 수요자의 요구 등에 의하여 변경되어져야 하며 프로그램의 학습 성과의 결과에 의하여 보완 수정되어야 한다. &lt;그림 2.4&gt;는 프로그램의 교육 목표의 개선에 구성원의 요구사항을 수렴하는 개념을 나타낸다.</p> 
<p class="pd10 mr05 tline2 style6">프로그램의 교육 목표에 대한 종합적인 평가 및 개선은 3년 마다 시행한다. 이에 따라 학습성과 및 교육과정(교과과정 개편 포함)의 주요 개선은 매 3년마다 이루어진다. 주요 구성원을 대상으로 한 조사 및 평가는 매년 시행하여 자료를 축적해오고 있다. </p> 


